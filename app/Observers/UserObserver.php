     * Handle the User "created" event.
     *
     * @param  \App\Models\User  $user
     * @return void
     */
    public function created(User $user)
    {
        $token = Str::random(64);

        UserVerify::create([
            'user_id' => $user->id, 
            'token' => $token
        ]);

        dispatch(new \App\Jobs\VerifyMailJob($user->email, ['token' => $token, 'user' => $user]));

        return $user;
    }

